<?php
// Проходим по всем кукам и удаляем их
foreach ($_COOKIE as $cookie_name => $cookie_value) {
    // Устанавливаем время истечения срока действия в прошлом (тем самым удаляя куку)
    setcookie($cookie_name, '', time() - 3600, '/'); // 3600 секунд = 1 час назад
}

// Перенаправляем на страницу, например, на главную или страницу логина
header("Location: ../index.php");
exit();
?>
